        adressNextOfKin = Repo.getLastAdressDetails();
        
        nok.setContactDetailsAdress(adressNextOfKin);
        buissnesClass.insertNextOfKin(nok);
        nok = Repo.getLastNextOfKin();
        patient.setNextOfKinBasic(nokBasic);
        
        JOptionPane.showMessageDialog(this, nok.toString());
        
//        
        PatientFull fullPatient = new PatientFull();
        
        fullPatient.setBasicComplaints(basicCompl);
