        nokBasic = Repo.getLastNextOfKinBasic();
        //patient.setNextOfKinBasic(nokBasic);
        
        nok.setNextOfKinBasic(nokBasic);
        
        JOptionPane.showMessageDialog(this, nokBasic.toString());
